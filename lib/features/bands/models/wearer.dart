class Wearer {
  final String uid;
  final String name;
  final String email;
  final String phone;
  final int age;
  final String address;
  final String bloodGroup;
  final String medicalConditions;
  final String doctorPhone;

  Wearer({
    required this.uid,
    required this.name,
    required this.email,
    required this.phone,
    required this.age,
    required this.address,
    required this.bloodGroup,
    required this.medicalConditions,
    required this.doctorPhone,
  });
}